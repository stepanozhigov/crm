    const STATUS_WAIT = 0;
    const STATUS_ACTIVE = 10;

    protected $fillable = [
        'name', 'email', 'first_name', 'last_name', 'status'
    ];

    protected $hidden = [
        'password', 'remember_token',
    ];

    protected $casts = [
        'email_verified_at' => 'datetime',
    ];

    public function isActive()
    {
        return $this->status === self::STATUS_ACTIVE;
    }

